use std::io;

fn main() {
    println!("Enter two numbers!");

    let mut num1 = String::new();
    let mut num2 = String::new();

    println!("Enter the first number:");
    io::stdin().read_line(&mut num1).expect("Failed to read line");

    println!("Enter the second number:");
    io::stdin().read_line(&mut num2).expect("Failed to read line");

    let num1: f64 = num1.trim().parse().expect("Please type a valid number");
    let num2: f64 = num2.trim().parse().expect("Please type a valid number");

    println!("Enter the operator (+, -, *, /):");
    let mut op = String::new();
    io::stdin().read_line(&mut op).expect("Failed to read line");

    let result: f64 = match op.trim() {
        "+" => num1 + num2,
        "*" => num1 * num2,
        "-" => num1 - num2,
        "/" => num1 / num2,
        _ => panic!("Invalid operator"),
    };

    println!("Result: {}", result);
}

